package Model;

public class Position {
    private int x;                    //Remember x is for rows, and y is for columns
    private int y;

    public Position(int x, int y){
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
